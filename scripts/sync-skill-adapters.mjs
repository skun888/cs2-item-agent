import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repositoryRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const canonicalDirectory = resolve(repositoryRoot, ".agents", "skills", "cs2-item-agent");
const canonicalSkillPath = resolve(canonicalDirectory, "SKILL.md");
const checkOnly = process.argv.includes("--check");

const adapters = [
  {
    name: "legacy",
    directory: resolve(repositoryRoot, "skills", "cs2-item-agent"),
  },
  {
    name: "claude-code",
    directory: resolve(repositoryRoot, ".claude", "skills", "cs2-item-agent"),
  },
];

const canonicalSkill = await readFile(canonicalSkillPath, "utf8");
const frontmatterMatch = canonicalSkill.match(/^---\r?\n[\s\S]*?\r?\n---/);
if (!frontmatterMatch) {
  throw new Error(`Canonical skill has invalid frontmatter: ${canonicalSkillPath}`);
}

function toPosixPath(path) {
  return path.replaceAll("\\", "/");
}

function renderAdapter(directory) {
  const canonicalRelativePath = toPosixPath(relative(directory, canonicalSkillPath));
  return `${frontmatterMatch[0].replaceAll("\r\n", "\n")}

# CS2 Item Agent adapter

Read [the canonical CS2 Item Agent skill](${canonicalRelativePath}) completely before handling a matching request. Resolve every linked reference from the canonical skill directory and follow it as written.

This adapter is generated by \`npm run skill:sync\`; do not edit it directly.
`;
}

const staleAdapters = [];
for (const adapter of adapters) {
  const adapterPath = resolve(adapter.directory, "SKILL.md");
  const expected = renderAdapter(adapter.directory);

  if (checkOnly) {
    let actual;
    try {
      actual = await readFile(adapterPath, "utf8");
    } catch {
      staleAdapters.push(`${adapter.name}: missing ${toPosixPath(relative(repositoryRoot, adapterPath))}`);
      continue;
    }
    if (actual.replaceAll("\r\n", "\n") !== expected) {
      staleAdapters.push(`${adapter.name}: stale ${toPosixPath(relative(repositoryRoot, adapterPath))}`);
    }
    continue;
  }

  await mkdir(adapter.directory, { recursive: true });
  await writeFile(adapterPath, expected, "utf8");
}

if (staleAdapters.length > 0) {
  throw new Error(`Skill adapters are not synchronized:\n${staleAdapters.join("\n")}`);
}

console.log(checkOnly ? "Skill adapters are synchronized." : "Skill adapters synchronized.");
